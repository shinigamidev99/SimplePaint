    private void jpnPaintMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpnPaintMouseReleased
        if(flagShape)
        {
            p2 = evt.getPoint();
            g.setColor(c);
            Graphics2D g1 = (Graphics2D) g;
            g1.setStroke(new BasicStroke(jsldSize.getValue()));

            if (jradLine.isSelected())
            {
                g.drawLine(p1.x, p1.y, p2.x, p2.y);
            }

            if (jradRectangle.isSelected()) 
            {
                int left = Math.min(p1.x, p2.x);
                int top = Math.min(p1.y, p2.y);
                int w = Math.abs(p1.x - p2.x);
                int h = Math.abs(p1.y - p2.y);

                if (jradDraw.isSelected()) 
                {
                    g.drawRect(left, top, w, h);
                } 
                else 
                {
                    g.fillRect(left, top, w, h);
                }
            }

            if (jradRoundRec.isSelected()) 
            {
                int left = Math.min(p1.x, p2.x);
                int top = Math.min(p1.y, p2.y);
                int w = Math.abs(p1.x - p2.x);
                int h = Math.abs(p1.y - p2.y);

                if (jradDraw.isSelected()) 
                {
                    g.drawRoundRect(left, top, w, h, 40, 45);
                } 
                else 
                {
                    g.fillRoundRect(left, top, w, h, 40, 45);
                }
            }

            if (jradOval.isSelected())
            {
                int left = Math.min(p1.x, p2.x);
                int top = Math.min(p1.y, p2.y);
                int w = Math.abs(p1.x - p2.x);
                int h = Math.abs(p1.y - p2.y);

                if (jradDraw.isSelected()) 
                {
                    g.drawOval(left, top, w, h);
                } 
                else
                {
                    g.fillOval(left, top, w, h);
                }
            }

            p1 = null;
            p2 = null;
        }
    }//GEN-LAST:event_jpnPaintMouseReleased

