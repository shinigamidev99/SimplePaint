    private void jpnPaintMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpnPaintMouseDragged
        if(flagPen)
        {
            p2 = evt.getPoint();
            Graphics2D g1 = (Graphics2D) g;
            g1.setStroke(new BasicStroke(jsldSize.getValue()));
            g.setColor(c);
            g.drawLine(p1.x, p1.y, p2.x, p2.y);
            p1 = p2;
        }
        
        if(flagEraser)
        {
            p2 = evt.getPoint();
            Graphics2D g1 = (Graphics2D) g;
            g1.setStroke(new BasicStroke(jsldSize.getValue()));
            g.setColor(Color.WHITE);
            g.drawLine(p1.x, p1.y, p2.x, p2.y);
            p1 = p2;
        }
    }//GEN-LAST:event_jpnPaintMouseDragged

